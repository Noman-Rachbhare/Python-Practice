# String Function

name="Noman"

# Length
print(len(name));

# EndWith
print(name.endswith("an"));

# StartWith
print(name.startswith("No"));

# Upper
print(name.upper());

# Lower
print(name.lower());

# Title
print(name.title());

# Count
print(name.count("n"));

# Find
print(name.find("a"));

# Replace
print(name.replace("Noman","Mateen"));

